public class Results
{
	public static void main
	( String [] args )
	{
	double amount = 1;
	double m = 50;
	double n = 10;
	double p = 5;
	
	System.out.println ("The results are " + n / p +3);
	System.out.println ("The results are " + (m / p + n - 10 * amount));
	System.out.println ("The results are " + (m - 3 * n + 4 * amount));
	System.out.println ("The results are " + amount / 5);
	System.out.println ("The results are " + 18 / p);
	System.out.println ("The results are " + 18 % p);
	System.out.println ("The results are " + (-p * n));
	System.out.println ("The results are " + -m/20);
	System.out.println ("The results are " + -m % 20);
	System.out.println ("The results are " + (m+n)/(p+amount));
	System.out.println ("The results are " + m+n/p+amount);
	}
}